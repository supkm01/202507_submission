package lesson3_2.stream_ex.practice1;

public class Person {

	//フィールド
	private String name;
	private int age;

	//コンストラクタ
	public Person(String name, int age) {
		this.name = name;
		this.age = age;
	}

	//get set 
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

}
